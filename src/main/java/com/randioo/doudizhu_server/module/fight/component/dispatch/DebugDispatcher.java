package com.randioo.doudizhu_server.module.fight.component.dispatch;

import java.util.ArrayList;
import java.util.List;

/**
 * 调试发牌器
 * 
 * @author AIM
 *
 */
public class DebugDispatcher implements Dispatcher {
    private int[][] card = {
            { 0x11, 0x21, 0x31, 0x41, 0x12, 0x22, 0x32, 0x42, 0x13, 0x23, 0x33, 0x43, 0x14, 0x24, 0x34, 0x44, 0x1D },
            { 0x15, 0x25, 0x35, 0x49, 0x16, 0x26, 0x36, 0x4A, 0x17, 0x27, 0x3B, 0x4B, 0x18, 0x28, 0x38, 0x48, 0x4C },
            { 0x19, 0x29, 0x39, 0x45, 0x1A, 0x2A, 0x3A, 0x46, 0x1B, 0x2B, 0x37, 0x47, 0x1C, 0x2C, 0x3C, 0x0E, 0x0F },
            { 0x19, 0x29, 0x39, 0x45, 0x1A, 0x2A, 0x3A, 0x46, 0x1B, 0x2B, 0x37, 0x47, 0x1C, 0x2C, 0x3C, 0x0E, 0x0F } };

    @Override
    public List<CardPart> dispatch(List<Integer> totalCards, int everyPartCount, int group) {
        List<CardPart> list = new ArrayList<>();
        for (int i = 0; i < group; i++) {
            CardPart cardPart = new CardPart();
            list.add(cardPart);
            for (int j = 0; j < everyPartCount; j++) {
                int card = this.card[i][j];
                cardPart.add(card);
            }
        }
        return list;
    }

}
